export function getFlightsByRoute(
  origin: string,
  destination: string
): Flight[] {
  const originUpper = origin.toUpperCase();
  const destUpper = destination.toUpperCase();
  const results = mockFlights.filter(
    (f) => f.origin === originUpper && f.destination === destUpper
  );

  if (results.length === 0) {
    return generateRandomFlights(originUpper, destUpper);
  }

  return results;
}

function generateRandomFlights(origin: string, destination: string): Flight[] {
  const airlines: Array<"LATAM" | "Gol" | "Azul"> = ["LATAM", "Gol", "Azul"];
  const flights: Flight[] = [];

  const hours = [6, 8, 10, 12, 14, 16, 18, 20];
  const durations = ["1h15", "1h30", "2h00", "2h30", "3h00", "3h30"];
  const basePrices = [350, 420, 480, 550, 650, 780];

  for (let i = 0; i < 4; i++) {
    const airline = airlines[i % 3];
    const hour = hours[i * 2];
    const duration = durations[i % durations.length];
    const basePrice = basePrices[i % basePrices.length];
    const depHour = hour;
    const arrHour = (hour + parseInt(duration) + 1) % 24;

    flights.push({
      id: `${airline[0]}${i}${origin}${destination}`,
      airline,
      flightNumber: `${airline === "LATAM" ? "LA" : airline === "Gol" ? "G3" : "AD"} ${1000 + i * 100}`,
      origin,
      destination,
      departureTime: `${String(depHour).padStart(2, "0")}:00`,
      arrivalTime: `${String(arrHour).padStart(2, "0")}:${i % 2 === 0 ? "00" : "30"}`,
      duration,
      stops: i > 2 ? 1 : 0,
      priceMoneyBRL: basePrice + Math.floor(Math.random() * 200),
      priceMilesLatamPass: airline === "LATAM" ? 8000 + i * 2000 : undefined,
      priceMilesSmiles: airline === "Gol" ? 8000 + i * 2000 : undefined,
      priceMilesTudoAzul: airline === "Azul" ? 8000 + i * 2000 : undefined,
      cabinClass: "economy",
      availableSeats: 5 + Math.floor(Math.random() * 20),
    });
  }

  return flights;
}
